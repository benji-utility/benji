#ifndef __BENJI_COLORS_H
#define __BENJI_COLORS_H

/* ANSI COLOR CODES */

#ifndef ANSI_BLACK
    #define ANSI_BLACK "\e[30m"
#endif

#ifndef ANSI_RED
    #define ANSI_RED "\e[31m"
#endif

#ifndef ANSI_GREEN
    #define ANSI_GREEN "\e[32m"
#endif

#ifndef ANSI_YELLOW
    #define ANSI_YELLOW "\e[33m"
#endif

#ifndef ANSI_BLUE
    #define ANSI_BLUE "\e[34m"
#endif

#ifndef ANSI_MAGENTA
    #define ANSI_MAGENTA "\e[35m"
#endif

#ifndef ANSI_CYAN
    #define ANSI_CYAN "\e[36m"
#endif

#ifndef ANSI_WHITE
    #define ANSI_WHITE "\e[37m"
#endif

#ifndef ANSI_COLOR_RESET
    #define ANSI_COLOR_RESET "\e[0m"
#endif

#endif